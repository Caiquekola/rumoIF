    private boolean usuarioExiste(Aluno a) {
            Connection con = ConnectionFactory.getConnection();
            PreparedStatement stmt = null;
            ResultSet rs = null;
            boolean existe = false;
            String sql = ("SELECT FROM rumoif.login WHERE nome = ? AND email = ? AND usuario = ? AND nivel = 0;");
            try {
            
            stmt = con.prepareStatement(sql);
            stmt.setString(1, a.getNome());
            stmt.setString(2, a.getEmail());
            stmt.setString(3, a.getUsuario());
            
            rs = stmt.executeQuery();
            if (rs.next()) {
                existe = true;
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(DiretorAlunoRemover.class.getName()).log(Level.SEVERE, null, ex);
        }finally{
               ConnectionFactory.closeConnection(con, stmt, rs); 
            }
        
        return existe;

    }

    private void remover(Aluno a) {
        
        if (jtNome.getText().isEmpty() || jtEmail.getText().isEmpty() || jtUsuario.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
        }else if (usuarioExiste(a)){
               AlunoDAO dao = new AlunoDAO();
               dao.delete(a);
               readJTable();
               jtNome.setText("");
               jtUsuario.setText("");
               jtEmail.setText("");
               JOptionPane.showMessageDialog(null, "Aluno removido com √™xito!");
        }else{
            JOptionPane.showMessageDialog(null, "Aluno n√£o existente!");
        }
            
        
        //Testar se usu√°rio existe

    }

    private Aluno obterAluno() {
            
            Aluno a = new Aluno(jtNome.getText(), jtEmail.getText(), jtUsuario.getText());
            return a;
        
    }
    
